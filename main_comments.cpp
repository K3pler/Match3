#include <SFML/Graphics.hpp>  // Библиотека SFML для графики (окно, фигуры, текст, цвета)
#include <cstdlib>            // Библиотека для функций rand() и srand() (генерация случайных чисел)
#include <ctime>              // Библиотека для функции time() (получение текущего времени)
#include <string>             // Библиотека для работы со строками (std::string)
#include <sqlite3.h>          // Библиотека SQLite для работы с базой данных

using namespace sf;  // Используем пространство имён SFML, чтобы не писать sf:: перед каждым классом

// ============================================================================
// ГЛОБАЛЬНЫЕ КОНСТАНТЫ (размеры игрового поля и окна)
// ============================================================================

const int SIZE = 6;        // Размер игрового поля 6x6 (6 строк и 6 столбцов)
                           // Выбрано 6 вместо 8 для простоты и ускорения обучения

const int TILE = 64;       // Размер одной клетки в пикселях (64 на 64 пикселя)

const int WIDTH = SIZE * TILE;    // Ширина окна = 6 * 64 = 384 пикселя

const int HEIGHT = SIZE * TILE + 80;  // Высота окна = 384 + 80 = 464 пикселя
                                      // +80 оставлено для текста (счёт, рекорды, подсказки)

// ============================================================================
// КЛАСС БАЗЫ ДАННЫХ ДЛЯ ХРАНЕНИЯ РЕКОРДОВ
// ============================================================================

class Database {
private:
    sqlite3* db;  // Указатель на объект базы данных SQLite
                  // nullptr означает, что база данных ещё не открыта

public:
    // Конструктор - вызывается при создании объекта Database
    // Инициализирует указатель db нулевым значением (nullptr)
    Database() : db(nullptr) {}

    // Функция открытия базы данных и создания таблицы
    void open() {
        // sqlite3_open - открывает (или создаёт) файл базы данных
        // "scores.db" - имя файла
        // &db - передаём указатель на указатель, функция запишет сюда адрес открытой БД
        sqlite3_open("scores.db", &db);
        
        // sqlite3_exec - выполняет SQL-запрос без возврата результата
        // "DROP TABLE IF EXISTS scores;" - удаляем старую таблицу, если она существует
        // 0,0,0 - это callback-функция и её аргументы (не нужны)
        sqlite3_exec(db, "DROP TABLE IF EXISTS scores;", 0, 0, 0);
        
        // CREATE TABLE - создаём новую таблицу с двумя колонками:
        // score - INT (целое число) - очки игрока
        // time - INT (целое число) - время игры в секундах
        sqlite3_exec(db, "CREATE TABLE scores(score INT, time INT);", 0, 0, 0);
    }

    // Функция сохранения рекорда в базу данных
    void save(int score, int time) {
        // Формируем SQL-запрос INSERT INTO
        // std::to_string(score) - преобразует число в строку
        // Пример: score=150, time=45 -> "INSERT INTO scores VALUES(150,45);"
        std::string sql = "INSERT INTO scores VALUES(" + 
                          std::to_string(score) + "," + 
                          std::to_string(time) + ");";
        
        // Выполняем запрос
        // .c_str() - преобразует std::string в C-строку (массив char)
        sqlite3_exec(db, sql.c_str(), 0, 0, 0);
    }

    // Функция получения лучшего рекорда (максимального счёта)
    int getBest() {
        int best = 0;  // Переменная для хранения лучшего результата
        
        sqlite3_stmt* stmt;  // Подготовленный запрос (структура для работы с результатом)
        
        // SELECT MAX(score) FROM scores - выбирает максимальное значение из колонки score
        // -1 - длина строки (-1 означает "до конца")
        // &stmt - указатель, куда запишется подготовленный запрос
        sqlite3_prepare_v2(db, "SELECT MAX(score) FROM scores;", -1, &stmt, 0);
        
        // sqlite3_step() - выполняет запрос и переходит к первой строке результата
        // SQLITE_ROW - константа, означающая "есть строка с данными"
        if (sqlite3_step(stmt) == SQLITE_ROW) {
            // sqlite3_column_int() - извлекает целое число из первого столбца (индекс 0)
            best = sqlite3_column_int(stmt, 0);
        }
        
        // sqlite3_finalize() - освобождает ресурсы подготовленного запроса
        sqlite3_finalize(stmt);
        
        return best;  // Возвращаем лучший счёт (если таблица пуста - вернётся 0)
    }

    // Функция закрытия базы данных
    void close() {
        if (db)               // Проверяем, что база данных открыта (db не nullptr)
            sqlite3_close(db); // Закрываем соединение
    }
};

// ============================================================================
// МАССИВ ЦВЕТОВ ДЛЯ ФИШЕК
// ============================================================================

// Создаём массив из 4 цветов (индексы 0,1,2,3)
Color colors[4] = {
    Color::Red,      // Индекс 0 - красный
    Color::Green,    // Индекс 1 - зелёный
    Color::Blue,     // Индекс 2 - синий
    Color::Yellow    // Индекс 3 - жёлтый
};

// ============================================================================
// ГЛАВНАЯ ФУНКЦИЯ - ТОЧКА ВХОДА В ПРОГРАММУ
// ============================================================================

int main() {
    // ========================================================================
    // НАЧАЛЬНАЯ НАСТРОЙКА
    // ========================================================================
    
    // srand(time(0)) - инициализация генератора случайных чисел
    // time(0) - возвращает количество секунд с 1970 года
    // Без этого rand() будет выдавать одинаковые числа при каждом запуске
    srand(time(0));
    
    // Создаём окно игры
    // VideoMode(WIDTH, HEIGHT) - задаём размер окна
    // "Match-3" - заголовок окна
    RenderWindow window(VideoMode(WIDTH, HEIGHT), "Match-3");
    
    // setFramerateLimit(60) - ограничиваем частоту обновления экрана до 60 кадров в секунду
    // Это нужно, чтобы игра не работала слишком быстро на мощных компьютерах
    window.setFramerateLimit(60);
    
    // ========================================================================
    // ЗАГРУЗКА ШРИФТА
    // ========================================================================
    
    Font font;  // Объект шрифта
    // loadFromFile() - загружает шрифт из файла
    // Путь к системному шрифту Arial в Windows
    font.loadFromFile("C:/Windows/Fonts/arial.ttf");
    
    // ========================================================================
    // ПОДКЛЮЧЕНИЕ БАЗЫ ДАННЫХ
    // ========================================================================
    
    Database db;   // Создаём объект базы данных
    db.open();     // Открываем базу данных (создаётся файл scores.db)
    
    // ========================================================================
    // СОЗДАНИЕ ИГРОВОГО ПОЛЯ
    // ========================================================================
    
    // board - двумерный массив 6x6 для хранения типов фишек
    // Значения: 0,1,2,3 - соответствуют цветам из массива colors
    // -1 означает пустую клетку (после удаления комбинации)
    int board[SIZE][SIZE];
    
    // Заполняем поле случайными фишками
    // rand() % 4 - даёт случайное число от 0 до 3
    for (int y = 0; y < SIZE; y++)        // y - строка (0..5)
        for (int x = 0; x < SIZE; x++)    // x - столбец (0..5)
            board[y][x] = rand() % 4;
    
    // ========================================================================
    // ИГРОВЫЕ ПЕРЕМЕННЫЕ
    // ========================================================================
    
    int score = 0;              // Текущий счёт игрока
    int moves = 0;              // Количество сделанных ходов (максимум 15)
    int selectedX = -1, selectedY = -1;  // Координаты выбранной фишки (-1 = ничего не выбрано)
    bool waiting = false;       // Ожидаем ли вторую фишку для обмена
    bool inGame = false;        // Идёт ли игра (true) или показываем меню (false)
    int startTime = time(0);    // Время начала игры (в секундах от 1970 года)
    
    // ========================================================================
    // СОЗДАНИЕ ТЕКСТОВЫХ ЭЛЕМЕНТОВ
    // ========================================================================
    
    // Заголовок игры в меню
    Text title("MATCH-3", font, 40);           // Текст, шрифт, размер
    title.setPosition(WIDTH/2 - 80, 30);       // Центрируем по горизонтали
    title.setFillColor(Color::Yellow);         // Жёлтый цвет
    
    // Подсказка "Нажмите пробел"
    Text info("Press SPACE to start", font, 20);
    info.setPosition(WIDTH/2 - 100, HEIGHT - 50);  // Внизу, по центру
    info.setFillColor(Color::Green);
    
    // Текст для отображения текущего счёта и ходов
    Text scoreText("", font, 20);
    scoreText.setPosition(10, HEIGHT - 40);     // Левый нижний угол
    scoreText.setFillColor(Color::White);
    
    // Текст для отображения лучшего рекорда
    Text bestText("", font, 20);
    bestText.setPosition(WIDTH - 150, HEIGHT - 40);  // Правый нижний угол
    bestText.setFillColor(Color::Yellow);
    
    // ========================================================================
    // ЛЯМБДА-ФУНКЦИЯ: ПОИСК КОМБИНАЦИЙ
    // ========================================================================
    
    // [&] - лямбда-функция захватывает все переменные по ссылке
    // (bool match[SIZE][SIZE]) - принимает массив для отметки комбинаций
    // -> bool - возвращает true, если есть хотя бы одна комбинация
    auto findMatches = [&](bool match[SIZE][SIZE]) -> bool {
        bool found = false;  // Флаг: нашли ли мы комбинации
        
        // Сначала сбрасываем все флаги match в false
        for (int y = 0; y < SIZE; y++)
            for (int x = 0; x < SIZE; x++)
                match[y][x] = false;
        
        // ПОИСК ГОРИЗОНТАЛЬНЫХ КОМБИНАЦИЙ (в строках)
        for (int y = 0; y < SIZE; y++) {           // Перебираем все строки
            for (int x = 0; x < SIZE - 2; x++) {   // До SIZE-2, чтобы не выйти за границы
                int kind = board[y][x];             // Тип текущей фишки
                if (kind == -1) continue;          // Пустую клетку пропускаем
                
                int len = 1;  // Начинаем с длины 1 (текущая фишка)
                // Считаем, сколько одинаковых фишек идёт подряд вправо
                while (x + len < SIZE && board[y][x + len] == kind)
                    len++;  // Увеличиваем длину, пока фишки одинаковые
                
                // Если длина >= 3 - нашли комбинацию
                if (len >= 3) {
                    found = true;  // Запоминаем, что есть комбинация
                    // Отмечаем все фишки в этой комбинации
                    for (int k = 0; k < len; k++)
                        match[y][x + k] = true;
                }
                // Пропускаем уже проверенную последовательность
                x += len - 1;
            }
        }
        
        // ПОИСК ВЕРТИКАЛЬНЫХ КОМБИНАЦИЙ (в столбцах)
        for (int x = 0; x < SIZE; x++) {           // Перебираем все столбцы
            for (int y = 0; y < SIZE - 2; y++) {   // До SIZE-2, чтобы не выйти за границы
                int kind = board[y][x];             // Тип текущей фишки
                if (kind == -1) continue;          // Пустую клетку пропускаем
                
                int len = 1;  // Начинаем с длины 1
                // Считаем, сколько одинаковых фишек идёт подряд вниз
                while (y + len < SIZE && board[y + len][x] == kind)
                    len++;
                
                if (len >= 3) {
                    found = true;
                    for (int k = 0; k < len; k++)
                        match[y + k][x] = true;
                }
                y += len - 1;
            }
        }
        return found;  // Возвращаем true, если есть комбинации
    };
    
    // ========================================================================
    // ЛЯМБДА-ФУНКЦИЯ: УДАЛЕНИЕ КОМБИНАЦИЙ
    // ========================================================================
    
    auto removeMatches = [&](bool match[SIZE][SIZE]) {
        for (int y = 0; y < SIZE; y++) {
            for (int x = 0; x < SIZE; x++) {
                if (match[y][x]) {           // Если фишка отмечена для удаления
                    board[y][x] = -1;        // Ставим -1 (пустая клетка)
                    score += 10;             // Добавляем 10 очков
                }
            }
        }
    };
    
    // ========================================================================
    // ЛЯМБДА-ФУНКЦИЯ: ГРАВИТАЦИЯ (фишки падают вниз)
    // ========================================================================
    
    auto gravity = [&]() {
        // Проходим по каждому столбцу
        for (int x = 0; x < SIZE; x++) {
            // Идём снизу вверх (с последней строки к первой)
            for (int y = SIZE - 1; y >= 0; y--) {
                if (board[y][x] == -1) {  // Если текущая клетка пустая
                    // Ищем сверху непустую фишку
                    for (int up = y - 1; up >= 0; up--) {
                        if (board[up][x] != -1) {  // Нашли непустую
                            board[y][x] = board[up][x];  // Копируем вниз
                            board[up][x] = -1;           // Очищаем исходную
                            break;  // Нашли одну - хватит
                        }
                    }
                }
            }
        }
    };
    
    // ========================================================================
    // ЛЯМБДА-ФУНКЦИЯ: ДОБАВЛЕНИЕ НОВЫХ ФИШЕК
    // ========================================================================
    
    auto refill = [&]() {
        for (int y = 0; y < SIZE; y++) {
            for (int x = 0; x < SIZE; x++) {
                if (board[y][x] == -1) {          // Если клетка пустая
                    board[y][x] = rand() % 4;     // Создаём новую случайную фишку
                }
            }
        }
    };
    
    // ========================================================================
    // ЛЯМБДА-ФУНКЦИЯ: КАСКАДНАЯ ОБРАБОТКА
    // ========================================================================
    
    auto processMatches = [&]() {
        bool match[SIZE][SIZE];                     // Временный массив для отметок
        while (findMatches(match)) {                // Пока есть комбинации
            removeMatches(match);                   // Удаляем их
            gravity();                              // Фишки падают
            refill();                               // Добавляем новые
        }
    };
    
    // ========================================================================
    // ГЛАВНЫЙ ИГРОВОЙ ЦИКЛ
    // ========================================================================
    
    // Цикл работает, пока окно открыто
    while (window.isOpen()) {
        
        // ====================================================================
        // ОБРАБОТКА СОБЫТИЙ (ввод с клавиатуры и мыши)
        // ====================================================================
        
        Event event;  // Переменная для хранения события
        // pollEvent() - забирает следующее событие из очереди
        while (window.pollEvent(event)) {
            
            // Закрытие окна (крестик)
            if (event.type == Event::Closed)
                window.close();
            
            // Нажатие клавиши ESC - выход из игры
            if (event.type == Event::KeyPressed && event.key.code == Keyboard::Escape)
                window.close();
            
            // Нажатие пробела в меню - начало новой игры
            if (event.type == Event::KeyPressed && 
                event.key.code == Keyboard::Space && !inGame) {
                
                // Заполняем поле случайными фишками
                for (int y = 0; y < SIZE; y++)
                    for (int x = 0; x < SIZE; x++)
                        board[y][x] = rand() % 4;
                
                // Убираем начальные комбинации
                processMatches();
                
                // Сбрасываем игровые переменные
                score = 0;
                moves = 0;
                inGame = true;      // Переключаемся в режим игры
                waiting = false;    // Ничего не выбрано
                startTime = time(0); // Запоминаем время старта
            }
            
            // ОБРАБОТКА КЛИКОВ МЫШИ (только во время игры)
            if (inGame && event.type == Event::MouseButtonPressed && 
                event.mouseButton.button == Mouse::Left) {
                
                // Получаем координаты клика
                // Mouse::getPosition(window) - позиция мыши в окне
                // делим на TILE (64) - получаем номер клетки
                int x = Mouse::getPosition(window).x / TILE;
                int y = Mouse::getPosition(window).y / TILE;
                
                // Проверяем, что клик в пределах поля
                if (x >= 0 && x < SIZE && y >= 0 && y < SIZE) {
                    
                    if (!waiting) {
                        // ПЕРВЫЙ КЛИК - запоминаем выбранную фишку
                        selectedX = x;
                        selectedY = y;
                        waiting = true;
                    } else {
                        // ВТОРОЙ КЛИК - проверяем, соседние ли фишки
                        bool adjacent = (selectedY == y && abs(selectedX - x) == 1) ||
                                        (selectedX == x && abs(selectedY - y) == 1);
                        
                        if (adjacent) {
                            // Меняем местами фишки
                            int temp = board[selectedY][selectedX];
                            board[selectedY][selectedX] = board[y][x];
                            board[y][x] = temp;
                            moves++;  // Увеличиваем счётчик ходов
                            
                            // Запоминаем старый счёт
                            int oldScore = score;
                            
                            // Обрабатываем комбинации
                            processMatches();
                            
                            // Если счёт не изменился - комбинаций не было
                            if (score == oldScore) {
                                // Откатываем обмен
                                temp = board[selectedY][selectedX];
                                board[selectedY][selectedX] = board[y][x];
                                board[y][x] = temp;
                                moves--;  // Возвращаем ход обратно
                            }
                        }
                        // Сбрасываем выбор
                        waiting = false;
                        selectedX = selectedY = -1;
                    }
                }
            }
        }
        
        // ====================================================================
        // ПРОВЕРКА ОКОНЧАНИЯ ИГРЫ (после 15 ходов)
        // ====================================================================
        
        if (moves >= 15 && inGame) {
            inGame = false;  // Выходим из режима игры
            // Сохраняем рекорд: счёт и затраченное время
            db.save(score, time(0) - startTime);
        }
        
        // ====================================================================
        // ОТРИСОВКА (рисование на экране)
        // ====================================================================
        
        // Очищаем экран тёмно-синим цветом (20,20,40) - RGB значения
        window.clear(Color(20, 20, 40));
        
        if (!inGame) {
            // РЕЖИМ МЕНЮ
            window.draw(title);   // Рисуем заголовок
            window.draw(info);    // Рисуем подсказку
            
            // Обновляем и рисуем лучший рекорд
            bestText.setString("Best: " + std::to_string(db.getBest()));
            window.draw(bestText);
        } else {
            // РЕЖИМ ИГРЫ
            
            // Рисуем все фишки на поле
            for (int y = 0; y < SIZE; y++) {
                for (int x = 0; x < SIZE; x++) {
                    if (board[y][x] >= 0) {  // Только непустые клетки
                        // Создаём прямоугольник (квадрат) для фишки
                        RectangleShape rect(Vector2f(TILE - 4, TILE - 4));
                        // Устанавливаем позицию (с отступом 2 пикселя)
                        rect.setPosition(x * TILE + 2, y * TILE + 2);
                        // Устанавливаем цвет по типу фишки
                        rect.setFillColor(colors[board[y][x] % 4]);
                        // Чёрная рамка вокруг фишки
                        rect.setOutlineColor(Color::Black);
                        rect.setOutlineThickness(2);
                        window.draw(rect);  // Рисуем прямоугольник
                    }
                }
            }
            
            // Рисуем жёлтую рамку вокруг выбранной фишки
            if (waiting && selectedX >= 0) {
                RectangleShape hl(Vector2f(TILE, TILE));
                hl.setPosition(selectedX * TILE, selectedY * TILE);
                hl.setFillColor(Color::Transparent);  // Прозрачная заливка
                hl.setOutlineColor(Color::Yellow);    // Жёлтая рамка
                hl.setOutlineThickness(3);
                window.draw(hl);
            }
            
            // Обновляем и рисуем счёт и количество ходов
            scoreText.setString("Score: " + std::to_string(score) + 
                               "  Moves: " + std::to_string(moves) + "/15");
            window.draw(scoreText);
        }
        
        // Отображаем всё нарисованное на экране
        window.display();
    }
    
    // ========================================================================
    // ЗАВЕРШЕНИЕ РАБОТЫ
    // ========================================================================
    
    db.close();   // Закрываем базу данных
    return 0;     // Возвращаем 0 - программа завершена успешно
}